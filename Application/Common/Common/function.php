}

/**
 * 修改文件内容
 * @param string $filename 文件名
 * @param mixed $need_editor 需要修改的内容
 * @param mixed $for_data 想要改成的内容
 * @return void
 */
function change_file_content($filename,$need_editor,$for_data){
    !file_exists($filename) ? $this->error('文件不存在') : null;
    file_put_contents($filename,str_replace($need_editor,$for_data, file_get_contents($filename)));
}